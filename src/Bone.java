public class Bone {

    private final int pip1;
    private final int pip2;
    private final int id;

    public Bone(int pip1, int pip2, int id) {
        this.pip1 = pip1;
        this.pip2 = pip2;
        this.id = id;
    }

    public int getPip1() {
        return pip1;
    }

    public int getPip2() {
        return pip2;
    }

    public int getId() {
        return id;
    }

}
